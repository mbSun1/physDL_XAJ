import torch
import torch.nn.functional as F

# =============================================================================
# 单位线卷积函数
# -----------------------------------------------------------------------------
# 作用：给定某一条河网单位线 UH，把"到达河道的入流序列"做一次卷积，
#       得到考虑河道传播/展宽之后的出口流量序列。
# 原理说明：
#   - 水文上的"单位线汇流"就是假定一单位产流进入河网后，会按一定的
#     时间分布到达出口（前升后降），这个时间分布就是 UH。
#   - 数学上就是离散卷积：Q_out[t] = Σ Q_in[t-k] * UH[k]
#   - PyTorch 的 conv1d 卷积核是按 w * x(t+τ) 的形式实现的，因此需要把
#     UH 沿时间反转，才能等价于水文里的"从过去到现在求和"。
#   - padding 的目的：保持卷积后序列长度不变，便于和原始的时间序列对齐。
# 注意：这里用了"分组卷积 groups=nb"，目的是让每个流域各自用自己的 UH，
#       而不是所有流域共享一条单位线。
# =============================================================================
def UH_conv(x, UH, viewmode=1):
    """
    参数
    ----
    x  : [batch, 1, time]  不同流域(或流域*组件)的一维入流序列
    UH : [batch, 1, uhLen] 与 x 对应的单位线
    返回
    ----
    y  : [batch, 1, time]  卷积后的出流序列
    """
    mm = x.shape
    nb = mm[0]           # batch 数 = 流域数（或者流域*组件数）
    m = UH.shape[-1]     # 单位线长度
    padd = m - 1         # 为了保持输出长度，需要 pad 的长度

    if viewmode == 1:
        # conv1d 的输入是 [N, C, L]，我们把所有流域拼到 N 维里
        xx = x.view([1, nb, mm[-1]])    # [1, batch, time]
        w = UH.view([nb, 1, m])         # [batch, 1, uhLen]
        groups = nb                     # 分组卷积，每个流域一组

    # 卷积：需要把单位线翻转，才能从"过去时刻"卷到"当前时刻"
    y = F.conv1d(xx,
                 torch.flip(w, [2]),
                 groups=groups,
                 padding=padd,
                 stride=1,
                 bias=None)
    # 去掉因为 padding 在末尾多出来的部分
    y = y[:, :, 0:-padd]
    # 还原形状
    return y.view(mm)


# =============================================================================
# Gamma 分布单位线
# -----------------------------------------------------------------------------
# 原理说明：
#   - 很多汇流单位线形状可以用 Gamma 分布来近似（左边陡升、右边缓降）。
#   - Gamma 的两个参数 a(形状) 和 b(尺度) 都有比较直观的控制作用：
#         a 越大 → 峰值后移、曲线更平滑
#         b 越大 → 展宽效果更明显
#   - 我们从模型中拿到的通常是 [0,1] 的无量纲参数，要把它们映射到
#     一个合理的 a,b 范围内，然后按 Gamma 公式生成一条长度为 lenF 的 UH。
#   - 生成完以后要对时间维归一化（Σ UH = 1），保证是"单位"线。
# =============================================================================
def UH_gamma(a, b, lenF=15):
    """
    参数
    ----
    a, b : [time, basin, 1]  每个流域的 a,b 参数（这里时间上其实都是重复的）
    lenF : 单位线长度
    返回
    ----
    UH   : [lenF, basin, 1]
    """
    m = a.shape
    # 为了数值稳定，a,b 不让它们是负的，同时给个下限
    aa = F.relu(a[0:lenF, :, :]) + 0.1
    theta = F.relu(b[0:lenF, :, :]) + 0.5

    # 时间轴，从 0.5 开始避免 t=0 导致 t^(a-1) 爆掉
    t = torch.arange(0.5, lenF * 1.0, device=a.device).view(lenF, 1, 1).repeat(1, m[1], m[2])

    # Gamma 分布公式：f(t) = t^(a-1) * exp(-t/θ) / (Γ(a)*θ^a)
    denom = (aa.lgamma().exp()) * (theta ** aa)
    mid = t ** (aa - 1)
    right = torch.exp(-t / theta)
    w = 1.0 / denom * mid * right

    # 每个流域一条单位线，沿时间归一化
    w = w / w.sum(0)
    return w
